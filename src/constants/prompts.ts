import { DOMAIN_OPTIONS, USECASE_CATEGORY_OPTIONS, REGION_OPTIONS, TECHNOLOGY_CATEGORY_OPTIONS, TECHNOLOGY_CATEGORY_LABELS } from './categories'

/**
 * 文献から新規事例JSONを生成するためのプロンプトテンプレートを返す。
 */
export function generateCreatePrompt(): string {
  const domainList = DOMAIN_OPTIONS.join(', ')
  const regionList = REGION_OPTIONS.join(', ')
  const categoryTable = USECASE_CATEGORY_OPTIONS.map((c) => `- ${c}`).join('\n')
  const techTable = TECHNOLOGY_CATEGORY_OPTIONS.map((key) => `- ${key}（${TECHNOLOGY_CATEGORY_LABELS[key]}）`).join('\n')

  return `# プライバシー強化技術（PETs）活用事例の作成

## 入力情報（※ ここに参考文献を追記してください）

以下の【 】内に、参考にする文献の情報を貼り付けてから、このプロンプトをAIに送信してください。
URL、PDFのテキスト、Webページの本文など、どの形式でも構いません。

【
ここに文献のURL、PDF本文、またはWebページのテキストを貼り付けてください。
例: https://example.com/privacy-tech-report.pdf
例: 論文タイトル「○○におけるプライバシー強化技術の活用」の本文テキスト
】

---

上記の情報から、プライバシー強化技術（PETs: Privacy Enhancing Technologies）の活用事例を抽出し、case.json を作成してください。

対象となる技術カテゴリ:
- 合成データ（Synthetic Data）
- 差分プライバシー（Differential Privacy）
- 匿名化（Anonymization / k-anonymity / l-diversity 等）
- 連合学習（Federated Learning）
- 秘密計算（Secure Computation / MPC / TEE 等）
- 分散データ分析（Distributed Analytics）

## 出力仕様

1件の事例につき1つのJSONを出力してください。文献中に複数の事例が含まれる場合は、事例ごとに分けて出力してください。

各JSONは以下のスキーマに従ってください：

\`\`\`json
{
  "title": "（後述のガイドラインに従って作成）",
  "region": "${regionList} のいずれか",
  "domain": "${domainList} のいずれか",
  "domain_sub": "分野のサブカテゴリ（任意。例: 保険, 製薬, がん臨床）",
  "organization": "実施組織名",
  "technology_category": ["下記の技術カテゴリから1つ以上"],
  "usecase_category": ["下記のユースケース分類から1つ以上"],
  "occurred_at": "事例の発生時期（YYYY, YYYY-MM, または YYYY-MM-DD。不明の場合は null）",
  "summary": "（後述のガイドラインに従って作成）",
  "value_proposition": "（後述のガイドラインに従って作成）",
  "privacy_enhancement_method": "プライバシー強化手法の説明（使用技術、パラメータ等）",
  "safety_assurance_method": "安全性保証手法（再識別リスク評価、監査、アクセス制御 等）",
  "utility_evaluation_method": "有用性の評価方法（精度比較, F値, 下流タスク評価 等）",
  "tags": ["検索用のキーワード2〜4個"],
  "sources": [
    {
      "source_type": "web or pdf",
      "title": "文献の正式タイトル",
      "url": "文献のURL",
      "note": "この文献からどの情報を参照したかの補足"
    }
  ],
  "figures": [
    {
      "type": "data_flow",
      "title": "（事例名）のPETs活用フロー",
      "data": {
        "nodes": [
          {"id": "s1", "label": "元データの説明",         "category": "source"},
          {"id": "s2", "label": "課題・制約の説明",        "category": "constraint"},
          {"id": "p1", "label": "プライバシー強化手法",     "category": "process"},
          {"id": "a1", "label": "データ・モデルの活用先",   "category": "application"},
          {"id": "a2", "label": "得られた成果・効果",       "category": "outcome"}
        ],
        "edges": []
      },
      "note": ""
    }
  ]
}
\`\`\`

## technology_category の選択肢

文献で使用されている技術から最も適切なものを選んでください（複数選択可）：

${techTable}

| 技術カテゴリ | 判断基準 |
|-------------|---------|
| synthetic_data | 元データの統計的特性を保持した合成データを生成して活用する |
| differential_privacy | ノイズ付加等により差分プライバシー保証を提供する |
| anonymization | k-匿名性、l-多様性、t-近接性等の匿名化手法を適用する |
| federated_learning | データを集約せず分散環境でモデルを学習する |
| secure_computation | 秘密分散、準同型暗号、TEE等で暗号化したままデータを処理する |
| distributed_analytics | データを移動させず各拠点で分析し結果のみを集約する |

## usecase_category の選択肢

文献の内容から最も適切なものを選んでください：

${categoryTable}

| カテゴリ | 判断基準 |
|---------|---------|
| 組織内データ共有 | 同一組織内の部門間でデータを共有するためにPETsを使う |
| 組織間データ共有 | 異なる組織（取引先、パートナー等）にデータを提供するためにPETsを使う |
| 外部分析者活用 | 外部の分析者・研究者にデータを提供して分析してもらう |
| R&D | 研究開発・学術研究の目的でPETsを活用する |
| データ販売 | プライバシーを保護したデータセットを商品として販売する |
| フィージビリティ検証 | 本格導入前の実現可能性検証・PoC用途でPETsを使う |
| 公的利用 | 行政・統計機関等が公的目的でPETsを活用する |

## domain の選択肢

${domainList}

## region の選択肢

${regionList}

## 記入ガイドライン

### title
- 先頭に事例の主体組織名を置き、コロン（: または ：）で区切って事例内容を続ける
  - 形式: \`{組織名}：{対象領域・目的・成果が分かる事例内容}\`
  - 例:「NEC：医療データ二次利用基盤の検証を目的とした医療合成データ活用」
  - 例:「Meta：広告主・小売双方のデータ非開示によるコンバージョン率計測」
- 複数組織の共同事例では主導組織を先頭に置き、必要なら「A社・B社：〜」のように列挙する
- 技術名（秘密計算、準同型暗号、差分プライバシー等）はタイトルに含めない。技術はtechnology_categoryで分類する
- 「誰のどんなデータをどう保護して何を実現したか」を軸にする
- 具体的な成果があれば盛り込む（例:「承認プロセスを6カ月から3日に短縮」）
- 組織名部分を含めて40〜60文字程度を目安にする

### summary
- 事例の「課題」セクションに表示される。課題の背景・状況を中心に記述する
- 「何が問題だったか」「なぜ困っていたか」をビジネスユーザーが理解できるように書く
- PETsの活用方法や解決策には触れない（それは value_proposition の役割）
- 2〜3文程度

### value_proposition
- 事例の「PETs適用により得られた価値」セクションに表示される
- PETsをどう適用して、何が実現できたかを記述する
- 具体的な技術手法名、数値があれば含める
- ビジネスユーザー目線で成果・効果を理解できるように書く
- 2〜3文程度
- **断定表現を避ける**:
  - 出典に記載された記述を引用する場合は「〜とされている」「〜と報告されている」など、出典に基づく伝聞表現を用いる
  - 文献から解釈・推論できる内容は「〜と考えられる」「〜が期待される」など、解釈であることが分かる表現を用いる
  - 「〜に価値がある」のような一面的な断定は避ける。価値は多面的で、記載した側面は複数ある価値のうちの一部である旨が伝わるように書く（例:「〜できる点が、本事例で示された価値の一つである」）
  - 結果や効果は、文献で確認できた範囲に限って記述する。将来の見通しや期待効果と実際の成果を混同しない

### occurred_at
- 文献の公開日や事例の実施時期から、事例の発生時期を記入する
- 年のみ判明: "2023"、月まで判明: "2023-06"、日まで判明: "2023-06-15"
- 時期が不明な場合は null とする

### data_flow 図のノード
- 各カテゴリに1〜3ノードが目安
- label は短く具体的に（15〜25文字程度）
- 数値があれば盛り込む（例:「有用性97%を達成」「6カ月→3日に短縮」）
- **constraint ノードの label は「課題と解決」セクションの見出しキーワードとして表示される。課題の核心を端的に表現すること**
- **outcome ノードの label は「得られた価値」セクションの見出しキーワードとして表示される。成果のハイライトを端的に表現すること**
- ノードIDは s1, s2（source列）、p1, p2（process列）、a1, a2（application/outcome列）の命名規則

### 情報が不明な場合
- 文献から読み取れない項目は「調査中」と記入する（空欄にしない）
- 無理に推測で埋めず、正確に分かる範囲だけを記入する

### tags
- 検索で引っかかるべきキーワードを2〜4個
- domain や usecase_category と重複しないものを選ぶ

## 出力形式

JSONのみを出力してください（マークダウンのコードブロックで囲む）。
複数事例の場合は、事例ごとに別のコードブロックで出力し、間に簡単な説明を挟んでください。`
}

/**
 * 既存事例を追加文献で補完・改善するためのプロンプトテンプレートを返す。
 */
export function generateEnrichPrompt(currentJson: string): string {
  return `# 既存事例の補完・改善

## 入力情報（※ ここに追加の参考文献を追記してください）

以下の【 】内に、追加の参考文献を貼り付けてから、このプロンプトをAIに送信してください。
URL、PDFのテキスト、Webページの本文など、どの形式でも構いません。

【
ここに追加文献のURL、PDF本文、またはWebページのテキストを貼り付けてください。
例: https://example.com/updated-report.pdf
】

---

上記の情報をもとに、以下の既存事例JSONを補完・改善してください。

## 既存の事例JSON
\`\`\`json
${currentJson}
\`\`\`

## 作業ルール

1. **更新対象**: すべての項目が改善の対象です。「調査中」の項目はもちろん、既存の記載がある項目でも、追加文献からより正確・詳細・具体的な情報が得られる場合は積極的に更新してください
2. **改善の例**:
   - 「調査中」→ 具体的な内容に更新
   - 抽象的な記述 → より具体的な記述（数値、手法名、組織名など）
   - 不完全な情報 → 文献で確認できた情報で補完
   - title は「{組織名}：{事例内容}」形式（技術名は含めない）。既存titleがこの形式でなければ揃える
   - summary は課題の背景・状況に焦点を当てて改善（PETsの解決策は含めない）
   - value_proposition はPETs適用による解決と成果に焦点を当てて改善。断定表現（「〜に価値がある」「〜を実現した」等）は、出典で確認できる範囲なら「〜とされている／報告されている」、解釈・推論なら「〜と考えられる／期待される」に置き換える
3. **technology_category の見直し**: 追加文献から、使用技術がより明確になった場合は technology_category を更新・追加してください
4. **occurred_at の補完**: 事例の発生時期が不明（null）で、追加文献から時期が判明した場合は occurred_at を記入してください（YYYY, YYYY-MM, YYYY-MM-DD 形式）
5. **sources の追加**: 追加文献を sources 配列に追記する（既存のsourceは削除しない）
6. **推測しない**: 文献に明記されていない情報は既存の記載を維持する（「調査中」はそのまま残す）

## data_flow 図の改善

既存の図のノードについても、追加文献の情報で label をより具体的にできる場合は更新してください。
例:
- 「プライバシー強化手法」→「CTGANで合成データを生成」（手法名が判明した場合）
- 「成果を達成」→「検出精度95%を達成」（数値が判明した場合）
- ノードの追加・分割も、情報が充実した場合は積極的に行ってください

**重要**: constraint / outcome ノードの label は詳細画面の見出しキーワードとしても使われるため、非技術者にも分かりやすい簡潔な表現にしてください

## 出力形式

更新後の完全なJSONを出力してください（マークダウンのコードブロックで囲む）。
JSONの後に、変更点のサマリーを箇条書きで示してください：
- 更新した項目と内容
- まだ「調査中」のまま残っている項目
- 新たに判明した情報の出典箇所`
}
